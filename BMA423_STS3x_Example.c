static uint32_t waiting_twi_done_timer_start(uint32_t timeout)
{
	return app_timer_start(m_waiting_twi_done_timer_id, APP_TIMER_TICKS(timeout, 0), NULL);
}

static uint32_t waiting_twi_done_timer_stop(void)
{
	return app_timer_stop(m_waiting_twi_done_timer_id);
}

static void waiting_twi_done_timer_event(void * p_context)
{
	UNUSED_PARAMETER(p_context);
	waiting_twi_done_timeout = true;
}

void waiting_twi_done_timer_init(void)
{
	uint32_t err_code;
	
	err_code = app_timer_create(&m_waiting_twi_done_timer_id, APP_TIMER_MODE_SINGLE_SHOT, waiting_twi_done_timer_event);
	APP_ERROR_CHECK(err_code); 
}

static uint8_t waiting_for(volatile bool *things_done, uint32_t timeout)
{
	uint8_t result = 0;
	
	waiting_twi_done_timer_start(timeout);
	
	while(waiting_twi_done_timeout == false)
	{
		if(true == *things_done)
		{
			*things_done = false;
			waiting_twi_done_timer_stop();
			break;
		}
	}

	if(waiting_twi_done_timeout == true)
	{
		result = 1;
	}
	else
	{
		result = 0;
	}
	return result;
}

/**
 * @brief TWI events handler example.
 */
void twi_handler(nrf_drv_twi_evt_t const * p_event, void * p_context)
{   
    switch(p_event->type)
    {
    case NRF_DRV_TWI_EVT_DONE:
        if (p_event->xfer_desc.type == NRF_DRV_TWI_XFER_TX)
        {
				    m_write_done = true;
        }
        else if(p_event->xfer_desc.type == NRF_DRV_TWI_XFER_RX)
        {
				    m_read_done = true;
        }
        break;
    default:
        break;        
    }   
}

uint8_t bma423_read_chip_id(uint8_t *id)
{	
	uint32_t err_code; // error code

	uint8_t reg[1] = {0x00};
	uint8_t twi_rx[1] = {0};
  
	err_code = nrf_drv_twi_tx(&m_twi_device, BMA423_ADDRESS, reg, sizeof(reg), true);  
  APP_ERROR_CHECK(err_code);
    
	if(waiting_for(&m_write_done, 500)) { return BMA423_WR_ERROR; }
	
	err_code = nrf_drv_twi_rx(&m_twi_device, BMA423_ADDRESS, twi_rx, sizeof(twi_rx));
	APP_ERROR_CHECK(err_code);
	
	if(waiting_for(&m_read_done, 500)) { return BMA423_RD_ERROR; }
		
	*id = twi_rx[0];
	
	return BMA423_OK;
}

