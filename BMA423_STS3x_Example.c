uint8_t stream_transfer_write(const uint8_t *stream_data, uint16_t index)
{
	uint32_t err_code; // error code
	uint8_t reg[3] = {0};
	uint8_t asic_msb = (uint8_t)((index / 2) >> 4);
	uint8_t asic_lsb = ((index / 2) & 0x0F);

	reg[0] = 0x5B;
	reg[1] = asic_lsb;
	err_code = nrf_drv_twi_tx(&m_twi_device, BMA423_ADDRESS, reg, 2, true);  
	APP_ERROR_CHECK(err_code);
	
	if(waiting_for(&m_write_done, 500)) { return BMA423_WR_ERROR; }

	reg[0] = 0x5C;
	reg[1] = asic_msb;
	err_code = nrf_drv_twi_tx(&m_twi_device, BMA423_ADDRESS, reg, 2, true);  
	APP_ERROR_CHECK(err_code);
	
	if(waiting_for(&m_write_done, 500)) { return BMA423_WR_ERROR; }

	return write_regs(0x5E, (uint8_t *)stream_data, 8);
}

