uint8_t bma423_initialize(void)
{
	uint8_t chip_id = 0;
	
	bma423_read_chip_id(&chip_id);
	
	if(0x13 == chip_id)
	{
		bma4_write_config_file();
		
		if(BMA423_OK != bma423_config_features_in())
		{
			NRF_LOG_PRINTF("BMA423 bma423_config_features_in Failed!\r\n");
			return BMA423_ERROR; 
		}

		if(BMA423_OK == bma423_acceleration_config())
		{
			if(BMA423_OK == bma423_set_accel_enable(SET_ENABLE)) 
			{
				return BMA423_OK;
			}
			else
			{ 
				NRF_LOG_PRINTF("BMA423 bma423_initialize Failed!\r\n");
				return BMA423_WR_ERROR; 
			}
		}
		else
		{
			NRF_LOG_PRINTF("BMA423 Acceleration Config Failed!\r\n");
			return BMA423_ERROR;
		}
	}
	else
	{
		NRF_LOG_PRINTF("BMA423 Chip ID:0x%02X Error!\r\n", chip_id);
		return BMA423_ERROR;
	}
}

