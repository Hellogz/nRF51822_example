/**
 * Calculate the temperature in Celsius from SHT3xD raw temperature.
 */
static float sts3xd_calculate_temp_c(uint8_t msb, uint8_t lsb)
{
    uint16_t val;
    float temp;

    val = msb << 8 | lsb;
    temp = 175.0f * ((float)val / 65535.0f) - 45.0f;

    return (temp);
}

int16_t sts3x_get_temp(void)
{
	uint32_t err_code; // error code
	uint32_t serial_number = 0;

	uint8_t reg[2] = {0x24, 0x00};
	uint8_t twi_rx[3] = {0};
	int16_t result = 0;
	float temp = 0.0;
	
	sts3x_hardware_reset();
	
	if(STS3X_ERROR == sts3x_read_serial_number(&serial_number))
	{
		sts3x_soft_reset();
		if(STS3X_ERROR == sts3x_read_serial_number(&serial_number))
		{
			NRF_LOG_PRINTF("SHT3X Communication Error\r\n");
			set_device_status(TEMP_MASK, DEVICE_FAULT);
			return 0xFFFF;
		}
	}
	
	err_code = nrf_drv_twi_tx(&m_twi_device, STS3X_ADDRESS, reg, sizeof(reg), true);  
    APP_ERROR_CHECK(err_code);

	if(waiting_for(&m_write_done, 500)) { return 1; }
	
	nrf_delay_ms(15);
	
	err_code = nrf_drv_twi_rx(&m_twi_device, STS3X_ADDRESS, twi_rx, 3);
	APP_ERROR_CHECK(err_code);
	
	if(waiting_for(&m_read_done, 500)) { return 2; }
	
	if(sts3x_check_crc(twi_rx, 2, twi_rx[2]) == 0)
	{
		temp = sts3xd_calculate_temp_c(twi_rx[0], twi_rx[1]);

		set_device_status(TEMP_MASK, DEVICE_NORMAL);
		result = (int16_t)(temp * 100);
	}
	else
	{
		set_device_status(TEMP_MASK, DEVICE_FAULT);
		result = 0xFFFF;
	}

	return result;
}

