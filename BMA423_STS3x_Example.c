// Generator polynomial for CRC
#define POLYNOMIAL  0x131 // P(x) = x^8 + x^5 + x^4 + 1 = 100110001

//-----------------------------------------------------------------------------
static uint8_t sts3x_calc_crc(uint8_t data[], uint8_t len)
{
  uint8_t bit;        // bit mask
  uint8_t crc = 0xFF; // calculated checksum
  uint8_t byteCtr;    // byte counter
  
  // calculates 8-Bit checksum with given polynomial
  for(byteCtr = 0; byteCtr < len; byteCtr++)
  {
    crc ^= (data[byteCtr]);
    for(bit = 8; bit > 0; --bit)
    {
      if(crc & 0x80) crc = (crc << 1) ^ POLYNOMIAL;
      else           crc = (crc << 1);
    }
  }
  
  return crc;
}

//-----------------------------------------------------------------------------
static uint8_t sts3x_check_crc(uint8_t data[], uint8_t len, uint8_t checksum)
{
  uint8_t crc;     // calculated checksum
  
  // calculates 8-Bit checksum
  crc = sts3x_calc_crc(data, len);
  
  // verify checksum
  if(crc != checksum) return 1;
  else                return 0;
}

uint32_t sts3x_read_serial_number(uint32_t* serial_number)
{
	uint32_t err_code; // error code

	uint8_t reg[2] = {0x37, 0x80};
	uint8_t twi_rx[6] = {0};
  
	err_code = nrf_drv_twi_tx(&m_twi_device, STS3X_ADDRESS, reg, sizeof(reg), true);  
  APP_ERROR_CHECK(err_code);
    
	if(waiting_for(&m_write_done, 500)) { return 1; }
	
	err_code = nrf_drv_twi_rx(&m_twi_device, STS3X_ADDRESS, twi_rx, sizeof(twi_rx));
	APP_ERROR_CHECK(err_code);
	
	if(waiting_for(&m_read_done, 500)) { return 2; }
	
	if((sts3x_check_crc(twi_rx, 2, twi_rx[2]) == 0) && (sts3x_check_crc(twi_rx+3, 2, twi_rx[5]) == 0))
	{
		*serial_number = twi_rx[0] << 24 | twi_rx[1] << 16 | twi_rx[3] << 8 | twi_rx[4];
		err_code = 0;
	}
	else
	{
		*serial_number = 0;
		err_code = 1;
	}
	
	return err_code;
}

